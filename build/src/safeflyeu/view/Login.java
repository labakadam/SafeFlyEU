    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String sql = "select * from operater where email = ? and lozinka = ?";

        try {
            Class.forName("safeflyeu.model.Operater");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/safeflyeu_hib", "edunova", "edunova");
            java.sql.PreparedStatement ps = conn.prepareStatement(sql);
            
            ps.setString(1, txtEmail.getText());
            ps.setString(2, new String(txtLozinka.getPassword()));

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {

                JOptionPane.showMessageDialog(null, "Dobrodošli " + txtEmail.getText() ,"Uspješno logiranje ", JOptionPane.PLAIN_MESSAGE);
                new Izbornik().setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Nije dobro korisničko ime ili lozinka " + txtEmail.getText(), "Niste uspješno logirani ", JOptionPane.ERROR_MESSAGE);
                ocistiPolja();
                
            }
            
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_btnLoginActionPerformed

